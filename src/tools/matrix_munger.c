int main(void)
{
  // Unshifted key
  for(int key=0;key<72;key++)
    {
      int row=key&7;
      int column=key/8;

      int trow=1+row*3;
      int tcol=3+column;

      int x;
      for(x=0;tcol;x++) if (matrix[trow][x]=='|') tcol--;
      char r1[6],r2[6];
      for(int o=0;o<6;o++) {
	r1[o]=matrix[trow][x+o];
	r2[o]=matrix[trow+1][x+o];
      }
      r1[5]=0; r2[5]=0;
      printf("key #%d : '%s','%s'\n",key,r1,r2);
    }
  return 0;
}
